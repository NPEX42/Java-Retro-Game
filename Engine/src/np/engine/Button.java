package np.engine;

public enum Button {
    /**
     * The left mouse button.
     */
    LEFT,

    /**
     * The right mouse button.
     */
    RIGHT,

    /**
     * The middle mouse button, or mouse wheel on many mouses, if available.
     */
    MIDDLE,

    /**
     * The first extra mouse button, if available.
     */
    XBUTTON1,

    /**
     * The second extra mouse button, if available.
     */
    XBUTTON2,
}
